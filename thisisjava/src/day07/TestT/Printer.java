package day07.TestT;

public class Printer {
	//메소드 오버로딩!!!
	static public void println(int p) {
		System.out.println(p);
	}
	static public void println(boolean p) {
		System.out.println(p);
	}
	static public void println(double p) {
		System.out.println(p);
	}
	static public void println(String p) {
		System.out.println(p);
	}
}
